package patternprogram;

public class RightTringleStarPattern {

	public static void main(String[] args) {
		int i,j,row=6;
		//outer loop for row
		for(i=0;i<row;i++) {
			//inner loop for column
			for( j=0;j<=i;j++) {
				
				System.out.print("*");
			}
				
				System.out.println();
				
				
			}
		}

	}


